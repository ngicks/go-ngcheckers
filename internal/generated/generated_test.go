package generated_test

import (
	"go/ast"
	"go/parser"
	"go/token"
	"testing"

	"github.com/ngicks/go-ngcheckers/internal/generated"
)

const genSrc = `// Code generated by stringer. DO NOT EDIT.

package a

type Gen struct {
	F int
}
`

const handSrc = `package a

type Hand struct {
	F int
}
`

// structPos returns the position of the first struct type literal in f.
func structPos(f *ast.File) token.Pos {
	var pos token.Pos
	ast.Inspect(f, func(n ast.Node) bool {
		if _, ok := n.(*ast.StructType); ok && !pos.IsValid() {
			pos = n.Pos()
			return false
		}
		return true
	})
	return pos
}

func TestFiles_Contains(t *testing.T) {
	fset := token.NewFileSet()
	gen, err := parser.ParseFile(fset, "gen.go", genSrc, parser.ParseComments)
	if err != nil {
		t.Fatal(err)
	}
	hand, err := parser.ParseFile(fset, "hand.go", handSrc, parser.ParseComments)
	if err != nil {
		t.Fatal(err)
	}

	files := generated.Collect(fset, []*ast.File{gen, hand})

	if !files.Contains(structPos(gen)) {
		t.Error("Contains(generated struct pos) = false, want true")
	}
	if files.Contains(structPos(hand)) {
		t.Error("Contains(hand-written struct pos) = true, want false")
	}
	if files.Contains(token.NoPos) {
		t.Error("Contains(NoPos) = true, want false")
	}
}

// TestFiles_NilReceiver documents that a nil *Files reports false rather than
// panicking, so callers need not special-case the empty case.
func TestFiles_NilReceiver(t *testing.T) {
	var files *generated.Files
	if files.Contains(token.NoPos) {
		t.Error("nil *Files Contains = true, want false")
	}
}

// TestCollect_RequiresComments verifies that without parser.ParseComments the
// generated marker is invisible, so nothing is recorded.
func TestCollect_RequiresComments(t *testing.T) {
	fset := token.NewFileSet()
	gen, err := parser.ParseFile(fset, "gen.go", genSrc, 0)
	if err != nil {
		t.Fatal(err)
	}
	files := generated.Collect(fset, []*ast.File{gen})
	if files.Contains(structPos(gen)) {
		t.Error("Contains = true without ParseComments, want false")
	}
}
